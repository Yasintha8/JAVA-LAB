package com.mycompany.personobj11;
public class Person 
{
    private  String name;
    private int id;
    
    public Person(String name,int id)
    {
        this.name=name;
        this.id=id;
    }
    
    public void setname(String name)
    {
        this.name=name;
    }
    public String getname()
    {
        return name;
    }
    
    public void setid(int id)
    {
        this.id=id;
    }
    public int getid()
    {
        return id;
    }
    
   

}
